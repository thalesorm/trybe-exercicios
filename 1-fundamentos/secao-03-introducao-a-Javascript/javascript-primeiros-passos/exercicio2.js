// Utilize if/else para escrever um código que retorne o maior de dois números. Defina, no começo do seu código, duas constantes com os valores que serão comparados.

const number1 = 12;
const number2 = 15;

if (number1 > number2) {
  console.log(
    "O primeiro número é igual a " + number1 + " e é maior que " + number2
  );
} else {
  console.log(
    "O segundo número é igual a " + number2 + " e é maior que " + number1
  );
}
